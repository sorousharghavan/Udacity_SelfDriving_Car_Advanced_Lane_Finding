def undistort(distorted_img, mtx, dist):
    # Use the OpenCV undistort() function to remove distortion
    return cv2.undistort(distorted_img, mtx, dist, None, mtx)

def get_persp_trans_matrices(src, dst):
    M = cv2.getPerspectiveTransform(src, dst)
    Minv = cv2.getPerspectiveTransform(dst, src)
    return M, Minv

# Define a function that takes an image, number of x and y points, 
# camera matrix and distortion coefficients
def corners_unwarp(img, mtx, dist, src, dst, nx=9, ny=6):
    undist = undistort(img, mtx, dist)

    # Grab the image shape
    img_size = (img.shape[1], img.shape[0])

    M, Minv = get_persp_trans_matrices(src, dst)

    # Warp the image using OpenCV warpPerspective()
    warped = cv2.warpPerspective(undist, M, img_size)

    # Return the resulting image and matrix
    return undist, warped, M, Minv

